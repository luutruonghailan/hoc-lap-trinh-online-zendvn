number_one      = int(input("Nhập số thứ nhất: "))
number_two      = int(input("Nhập số thứ hai: "))
number_three    = int(input("Nhập số thứ ba: "))

max_number      = max(number_one, number_two, number_three)

print(f"Số lớn nhất trong 3 số {number_one}, {number_two}, {number_three} là {max_number}.")