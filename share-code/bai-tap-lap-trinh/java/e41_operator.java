package baitaplaptrinh;

public class e41_operator {
	public static void main(String[] args) {
		int numberOne    = 5;
		int numberTwo    = 2;
	
		System.out.println(numberOne + " + "  + numberTwo + " = " + (numberOne + numberTwo));
		System.out.println(numberOne + " - "  + numberTwo + " = " + (numberOne - numberTwo));
		System.out.println(numberOne + " * "  + numberTwo + " = " + (numberOne * numberTwo));
		System.out.println(numberOne + " / "  + numberTwo + " = " + ((double)numberOne / numberTwo));
		System.out.println(numberOne + " % "  + numberTwo + " = " + (numberOne % numberTwo));
	}
}